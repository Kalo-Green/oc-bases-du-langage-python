# Définition des variables
nom = "John Doe"
age = 25

# Affichage d'un message avec les valeurs des variables 'nom' et 'age'
# La notation f-string (chaîne de caractères formatée) permet d'insérer directement 
# les valeurs des variables dans la chaîne de caractères sans avoir besoin de les 
# concaténer manuellement (comme dans 'str(variable)' + 'texte')
print(f"Je m'appelle {nom} et j'ai {age} ans.")

# Augmentation de l'âge de 10
age = age + 10

# Affichage du nouveau message avec l'âge mis à jour
print(f"Je m'appelle {nom} et j'ai {age} ans maintenant.")